const assert = require("node:assert/strict");
const fs = require("node:fs");
const os = require("node:os");
const path = require("node:path");
const test = require("node:test");

const {
  applyCompileDraft,
  createCompileDraft,
  routeRecordsForRole
} = require("../dist/core/compiler.js");
const { appendRecord, ensureStore, readAllRecords } = require("../dist/core/store.js");

function tempRoot() {
  const root = fs.mkdtempSync(path.join(os.tmpdir(), "wikiwiki-compile-"));
  writeFile(root, "README.md", "# Test repo\n");
  writeFile(root, "src/core/store.ts", "export {}\n");
  writeFile(root, "src/cli/commands/compile.ts", "export {}\n");
  return root;
}

function writeFile(root, file, content) {
  const fullPath = path.join(root, file);
  fs.mkdirSync(path.dirname(fullPath), { recursive: true });
  fs.writeFileSync(fullPath, content, "utf8");
}

