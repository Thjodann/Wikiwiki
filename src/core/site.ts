export function buildSiteFiles(root: string, options: SiteOptions = {}): SiteFile[] {
  const records = readAllRecords(root);
  const model = createSiteModel(root, records, resolveSiteOptions(root, options));
  const files: SiteFile[] = [];

  files.push({
    fileName: "index.html",
    content: renderLayout({
      model,
      fileName: "index.html",
      title: model.siteTitle,
      description: model.siteDescription,
      active: "home",
      body: renderHomePage(model)
    })
  });

  files.push({
    fileName: "guides.html",
    content: renderLayout({
      model,
      fileName: "guides.html",
      title: "Guides",
      description: `Curated paths through ${model.repoName} knowledge.`,
      active: "guides",
      body: renderGuidesPage(model)
    })
  });

  for (const category of categories) {
    files.push({
      fileName: category.fileName,
      content: renderLayout({
        model,
        fileName: category.fileName,
        title: category.title,
        description: category.description,
        active: category.type,
        body: renderCategoryPage(model, category)
      })
    });
  }

  if (showBeadsWork(model)) {
    files.push({
      fileName: "work.html",
      content: renderLayout({
        model,
        fileName: "work.html",
        title: "Project Work",
        description: "Developer-only Beads task context for this project.",
        active: "work",
        body: renderWorkPage(model)
      })
    });
  }

  files.push({
    fileName: "search.html",
    content: renderLayout({
      model,
      fileName: "search.html",
      title: "Search",
      description: "Search this project wiki without a backend.",
      active: "search",
      body: renderSearchPage()
    })
  });

  for (const record of model.flatRecords) {
    const fileName = recordHref(record);
    files.push({
      fileName,
      content: renderLayout({
        model,
        fileName,
        title: recordTitle(record),
        description: recordSummary(record),
        active: record.type,
        body: renderRecordPage(record, model, fileName)
      })
    });
  }

  files.push(
    { fileName: "favicon.svg", content: faviconSvg(model) },
    { fileName: "assets/wikiwiki.css", content: siteCss() },
    { fileName: "assets/project-theme.css", content: projectThemeCss(model.options.theme) },
    { fileName: "assets/search-index.js", content: searchIndexJs(model) },
    { fileName: "assets/wikiwiki.js", content: siteJs() },
    { fileName: ".nojekyll", content: "" },
    { fileName: "site-manifest.json", content: `${JSON.stringify(siteManifest(model), null, 2)}\n` }
  );

  return files;
}

export function renderSite(root: string, options: SiteOptions = {}): string[] {
  const files = buildSiteFiles(root, options);
  const outputPath = sitePath(root);

  fs.rmSync(outputPath, { recursive: true, force: true });
  fs.mkdirSync(outputPath, { recursive: true });

  return files.map((file) => {
    const outputFile = path.join(outputPath, file.fileName);
    fs.mkdirSync(path.dirname(outputFile), { recursive: true });
    fs.writeFileSync(outputFile, file.content, "utf8");
    return outputFile;
  });
}

export function resolveSiteOptions(root: string, options: SiteOptions = {}): ResolvedSiteOptions {
  const config = readWikiwikiConfig(root);
  return {
    sourceBaseUrl: normalizeSourceBaseUrl(options.sourceBaseUrl ?? config.source_base_url),
    theme: readWikiwikiSiteTheme(root),
    audience: parseSiteAudience(options.audience ?? config.site_audience, "all")
  };
}

function createSiteModel(root: string, records: RecordsByType, options: ResolvedSiteOptions): SiteModel {
  const audienceRecords = filterRecordsByAudience(records, options.audience);
  const integrations = readIntegrations(root, readWikiwikiConfig(root));
  const flatRecords: AnyRecord[] = [];
  for (const type of recordTypes) {
    flatRecords.push(...audienceRecords[type]);
  }
  flatRecords.sort(compareRecords);
  const hrefById = new Map(flatRecords.map((record) => [record.id, recordHref(record)]));
  const counts = Object.fromEntries(recordTypes.map((type) => [type, audienceRecords[type].length])) as Record<RecordType, number>;
  const repoName = options.theme.project_name?.trim() || path.basename(root);

  return {
    root,
    repoName,
    siteTitle: `${repoName} Wiki`,
    siteDescription: options.theme.project_description?.trim() || "A project wiki generated from durable repo knowledge.",
    projectInitials: initialsForName(repoName),
    records: audienceRecords,
    flatRecords,
    hrefById,
    counts,
    visibleCategories: categories.filter((category) => counts[category.type] > 0),
    options,
    audience: options.audience,
    integrations
  };
}

